package org.example;

public class Calculator {

    // პირველი მეთოდი: ორი რიცხვის გამრავლება
    public int multiply(int a, int b) {
        return a * b;
    }

    // მეორე მეთოდი: სამი რიცხვის გამრავლება (გადატვირთული)
    public int multiply(int a, int b, int c) {
        return a * b * c;
    }
}